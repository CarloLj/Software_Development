    private void jList1ValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jList1ValueChanged
        int selected = jList1.getSelectedIndex();
        registrotext.setText(ControlEscolar.profesores[selected].getRegistro());
        nombretext.setText(ControlEscolar.profesores[selected].getNombre());
        domiciliotext.setText(ControlEscolar.profesores[selected].getDomicilio());
        telefonotext.setText(ControlEscolar.profesores[selected].getTelefono());
        emailtext.setText(ControlEscolar.profesores[selected].getEmail());
        boolean sex  = ControlEscolar.profesores[selected].isSexo();
        int gradoacademico = ControlEscolar.profesores[selected].getGradoestudio();
        if(sex){
            sexotext.setText("Hombre");
        }else{
            sexotext.setText("Mujer");
        }
        switch(gradoacademico){
            case 1:
                gradoacademicotext.setText("Licenciatura");
                break;
            case 2:
                gradoacademicotext.setText("MaestrÃ­a");
                break;
            case 3:
                gradoacademicotext.setText("Doctorado");
                break;
        }
    }//GEN-LAST:event_jList1ValueChanged

